
export class Calc {

    public static sum(n1: number, n2: number) {
        return n1 + n2;
    }

    public static minus(n1: number, n2: number) {
        return n1 - n2;
    }

    public static divide(n1: number, n2: number) {
        return n1 / n2;
    }


    public static multiply(n1: number, n2: number) {
        return n1 * n2;
    }
}